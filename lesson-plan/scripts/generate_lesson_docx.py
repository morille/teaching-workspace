def replace_cell_paragraphs(cell, lines: list[str]) -> None:
    paragraphs = cell.paragraphs
    if len(lines) > len(paragraphs):
        raise ValueError(f"Too many lines for template cell: {len(lines)} > {len(paragraphs)}")
    for index, paragraph in enumerate(paragraphs):
        text = lines[index] if index < len(lines) else ""
        # Preserve paragraph/run style from the hand-tuned template.
        paragraph.text = text


def generate(template: Path, output: Path, lesson: LessonData) -> None:
    shutil.copyfile(template, output)
    doc = Document(output)

    if len(doc.sections) > 1 and doc.sections[1].header.paragraphs:
        doc.sections[1].header.paragraphs[0].text = (
            "吉林动画学院 本科课程教案                                                 2025-2026学年第二学期"
        )

    info = doc.tables[1]
    info.cell(2, 1).paragraphs[0].text = "2025/2026第二学期"

    main = doc.tables[3]
    main.cell(0, 1).paragraphs[0].text = lesson.lesson_no
    main.cell(0, 3).paragraphs[0].text = lesson.title
    main.cell(0, 5).paragraphs[0].text = "4"
    replace_cell_paragraphs(main.cell(2, 0), lesson.objective_lines)
    replace_cell_paragraphs(main.cell(4, 0), lesson.content_lines)
    replace_cell_paragraphs(main.cell(4, 4), lesson.design_lines)

    doc.save(output)


def main() -> None:
    if len(sys.argv) != 4:
        raise SystemExit(
            "Usage: generate_lesson_docx.py <lesson_no> <template.docx> <output.docx>"
        )
    lesson_no = sys.argv[1]
    template = Path(sys.argv[2])
    output = Path(sys.argv[3])
    if lesson_no not in LESSONS:
        raise SystemExit(f"Unsupported lesson: {lesson_no}")
    generate(template, output, LESSONS[lesson_no])
    print(output)


if __name__ == "__main__":
    main()
